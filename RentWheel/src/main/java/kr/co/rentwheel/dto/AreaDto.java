package kr.co.rentwheel.dto;

public class AreaDto {
	private int a_id;
	private String a_name;
	private String a_x_coord;
	private String a_y_coord;
	private String a_img;
	public int getA_id() {
		return a_id;
	}
	public void setA_id(int a_id) {
		this.a_id = a_id;
	}
	public String getA_name() {
		return a_name;
	}
	public void setA_name(String a_name) {
		this.a_name = a_name;
	}
	public String getA_x_coord() {
		return a_x_coord;
	}
	public void setA_x_coord(String a_x_coord) {
		this.a_x_coord = a_x_coord;
	}
	public String getA_y_coord() {
		return a_y_coord;
	}
	public void setA_y_coord(String a_y_coord) {
		this.a_y_coord = a_y_coord;
	}
	public String getA_img() {
		return a_img;
	}
	public void setA_img(String a_img) {
		this.a_img = a_img;
	}
	
}
